"""# Section 6 - Core Generation Function"""

def generate_dataset(config, clusters):
    """
    Generates the full synthetic dataset and returns a pandas DataFrame.

    Steps:
      1. Split clusters by label type.
      2. Distribute post count evenly across clusters within each label group
         (so every narrative type has equal representation).
      3. For each post: pick a random template, fill it, add all metadata.
      4. Shuffle so the CSV row order is random — important if a team member
         accidentally trains without shuffling the data themselves.
    """
    total     = config["total_posts"]
    misinfo_n = int(total * config["misinfo_ratio"])
    factual_n = total - misinfo_n

    misinfo_clusters = {k: v for k, v in clusters.items() if v["label"] == "misinformation"}
    factual_clusters = {k: v for k, v in clusters.items() if v["label"] == "factual"}

    per_misinfo = misinfo_n // len(misinfo_clusters)
    per_factual = factual_n // len(factual_clusters)

    records = []

    def generate_from_cluster(cluster_name, cluster_data, n):
        label     = cluster_data["label"]
        templates = cluster_data["templates"]
        for _ in range(n):
            template = random.choice(templates)
            text, location, region, route = fill_template(template)
            platform = random.choices(
                config["platforms"],
                weights=config["platform_weights"],
                k=1
            )[0]
            records.append({
                "post_id":            str(uuid.uuid4()),
                "text":               text,
                "cluster":            cluster_name,
                "label":              label,
                "platform":           platform,
                "region":             region,
                "location_mentioned": location,
                "route_mentioned":    route,
                "hashtags":           build_hashtags(cluster_name, label),
                "timestamp":          random_timestamp(config["date_start"], config["date_end"]),
                "engagement_score":   simulate_engagement(label),
                "username":           fake_username(label),
            })

    for name, data in misinfo_clusters.items():
        generate_from_cluster(name, data, per_misinfo)
    for name, data in factual_clusters.items():
        generate_from_cluster(name, data, per_factual)

    # Top up any posts lost to floor division
    remainder = total - len(records)
    all_names = list(clusters.keys())
    for _ in range(remainder):
        chosen = random.choice(all_names)
        generate_from_cluster(chosen, clusters[chosen], 1)

    random.shuffle(records)

    df = pd.DataFrame(records)
    return df[[
        "post_id", "text", "cluster", "label", "platform",
        "region", "location_mentioned", "route_mentioned",
        "hashtags", "timestamp", "engagement_score", "username",
    ]]

