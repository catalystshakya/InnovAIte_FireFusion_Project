#Template Function
def fill_template(template):
    """
    Swap {placeholder} tokens with random values from the Victorian word banks.

    Returns:
        text             (str)       — the filled post text
        location_found   (str|None)  — specific location injected, or None
        region_found     (str|None)  — region injected, or None
        route_found      (str|None)  — road/route injected, or None
    """
    location = random.choice(VIC_LOCATIONS)
    region   = random.choice(VIC_REGIONS)
    route    = random.choice(VIC_ROUTES)
    agency   = random.choice(VIC_AGENCIES)
    number   = random.choice(STATISTICS)
    source   = random.choice(FACTUAL_SOURCES)
    animal   = random.choice(VIC_ANIMALS)
    infra    = random.choice(INFRA_TYPES)
    operator = random.choice(INFRA_OPERATORS)

    text = (template
            .replace("{location}", location)
            .replace("{region}",   region)
            .replace("{route}",    route)
            .replace("{agency}",   agency)
            .replace("{number}",   number)
            .replace("{source}",   source)
            .replace("{animal}",   animal)
            .replace("{infra}",    infra)
            .replace("{operator}", operator))

    return (
        text,
        location if "{location}" in template else None,
        region   if "{region}"   in template else None,
        route    if "{route}"    in template else None,
    )

# Hashtag FUNCTION

def build_hashtags(cluster, label):
    """
    Generate a realistic hashtag string for a post.

    Combines:
      1. Cluster-specific tags (topical precision)
      2. General misinfo or factual tags (broader signal)

    Returns a comma-separated string, e.g.:
      "#VicEmergency,#CFA,#FireUpdate,#BushfireSafety"

    WHY STORE AS COMMA-SEPARATED STRING:
    CSV cells can't hold lists natively. Comma-separated is the standard
    approach — the modelling team can split on ',' to parse them if needed.

    We pick 2–3 cluster-specific tags and 1–2 general tags, then shuffle
    so the order isn't always the same (more realistic).
    """
    cluster_tags = HASHTAGS_BY_CLUSTER.get(cluster, [])
    if label == "misinformation":
        general_tags = HASHTAGS_MISINFO_GENERAL
    else:
        general_tags = HASHTAGS_FACTUAL_GENERAL

    # Sample a realistic number of each type
    chosen_cluster  = random.sample(cluster_tags,  min(random.randint(2, 3), len(cluster_tags)))
    chosen_general  = random.sample(general_tags,  min(random.randint(1, 2), len(general_tags)))

    combined = chosen_cluster + chosen_general
    random.shuffle(combined)
    return ",".join(combined)

# Timestamp

def random_timestamp(start, end):
    """
    Return a random ISO 8601 datetime string between start and end.
    ISO 8601 format is unambiguous and understood by pandas, SQL, and APIs.
    """
    delta = end - start
    random_seconds = random.randint(0, int(delta.total_seconds()))
    ts = start + timedelta(seconds=random_seconds)
    return ts.strftime("%Y-%m-%dT%H:%M:%S")


def simulate_engagement(label):
    """
    Simulate a combined likes + shares engagement score.

    Misinformation spreads faster and further than corrections on social
    media — we encode this as a higher-mean log-normal distribution for
    misinfo (see Reference [4]). Occasional very high values reflect
    real viral spikes.
    """
    if label == "misinformation":
        raw = random.lognormvariate(mu=4.5, sigma=1.2)
    else:
        raw = random.lognormvariate(mu=3.2, sigma=0.9)
    return max(int(raw), 0)

